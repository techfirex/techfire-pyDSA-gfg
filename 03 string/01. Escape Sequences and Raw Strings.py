# Escape Sequences
# s1 = 'welcome to geek's course' # invalid syntax
# print(s1)

s2 = 'welcome to geek\'s course' # corrected syntax
print(s2)

s3 = "Hi, \nWelcome to the course"
print(s3)

s4 = "A simple \ example"
print(s4)

s5 = "Backslash at the end \\"
print(s5)

s6 = "\\n"
print(s6)

s7 = "\\t"
print(s7)

# Raw Strings
r1 = "C:\project\name.py"
print(r1)

r2 = r"C:\project\name.py"
print(r2)

